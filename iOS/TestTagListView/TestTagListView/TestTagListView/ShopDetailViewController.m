#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 2;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return section == 0 ? 1 : 4;
}

 - (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
     UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell" forIndexPath:indexPath];
     
     // Configure the cell...
     if (indexPath.section == 0) {
         UIImageView *imgView = [[UIImageView alloc] initWithImage:[UIImage imageNamed:@"gameofthrones.jpg"]];
         imgView.frame = CGRectMake(0, 0, self.view.frame.size.width, 300);
         [cell addSubview:imgView];
         
         UIView* coverView = [[UIView alloc] initWithFrame:CGRectMake(0, 0, self.view.frame.size.width, 100)];
         // change the background color to black and the opacity to 0.6
         coverView.backgroundColor = [[UIColor redColor] colorWithAlphaComponent:0.3];
         // add this new view to your main view
         [cell addSubview:coverView];
         
         
     }
     
     return cell;
 }

-(CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath {
    return indexPath.section == 0 ? 300 : 50;
}

